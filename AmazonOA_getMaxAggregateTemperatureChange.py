# O(n) refined solution...
def getMaxAggregateTemperatureChange(tc):
    l = len(tc)
    maxAg = 0
    pdSum = 0
    tcSum = 0

    #start with sum of all nums in tc and then decrement as moving along
    for num in tc:
        tcSum += num

    for i in range(l-1):
        pdSum += tc[i]

        # O(3) elements is negligible
        maxAg = max(maxAg, pdSum, tcSum)

        tcSum -= tc[i]

    return maxAg


def getMaxAggregateTemperatureChange_bruteForce(tc):
    # FAILS WITH DATA SETS LARGER THAN 5 or so
    l = len(tc)
    maxAg = 0
    pd = [] #preceding days

    # O(n^2) bc of sum() function inside this
    for i in range(l-1):
        
        pd.append(tc[i]) # O(1)

        # max is O(n) but should be O(2) always bc its just 2 ints compared, 
        # but the sums inside it are O(n), so total is .... O(2n + 2) == O(n)
        maxAg = max(sum(pd), sum(tc))

        tc.pop(0) # O(1)

    return maxAg

